    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection con = CommonDb.getConnection();

        try {
            PreparedStatement pr = con.prepareStatement("select * from product_detail where pest_name=?");
            pr.setString(1, searchField.getText());
            ResultSet rs = pr.executeQuery();

            while (rs.next()) {
                id = rs.getString("p_id");
                String product = rs.getString("product_name");
                String pest = rs.getString("pest_name");
                String crop = rs.getString("crop");
                String trap = rs.getString("trap");
                String duration = rs.getString("expires");
                String cost = rs.getString("Cost");
                String per_acre = rs.getString("per_acre");

                String tbData[] = {id, product, pest, crop, trap, duration, cost, per_acre};
                DefaultTableModel tbModel = (DefaultTableModel) pTable.getModel();
                tbModel.addRow(tbData);
                System.out.println("Rest of the code" + id);
            }
            System.out.println("Record Found");
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

